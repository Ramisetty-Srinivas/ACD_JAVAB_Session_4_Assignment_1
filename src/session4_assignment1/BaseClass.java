package session4_assignment1;

/*
 * Child class has the same method as of base class. 
 * In such cases child class overrides the parent class method without even touching the source code of the base class. 
 * This feature is known as method overriding.
 */

/*
 * BaseClass for MethodOverRiding Example
 */

public class BaseClass {
	
    public void methodToOverride() //Base class method
    {
         System.out.println ("I'm the method of BaseClass");
    }
}
